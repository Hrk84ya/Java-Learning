// Multiple Variables in JAVA
public class MultiVariable {
    public static void main(String[] args){
        int a=10;
        int b=20, c=30, d=40;
        int e,f,g,h;
        e=f=g=h=50;

        System.out.println(a+b);
        System.out.println(c*d);
        System.out.println(e+f);
        System.out.println(g);
        System.out.println(h);

    }
}
